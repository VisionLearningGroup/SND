def apply_op(image, op, severity):
  image = np.clip(image, 0, 255).astype(np.uint8)
  pil_img = Image.fromarray(image)  # Convert to PIL.Image
  pil_img = op(pil_img, severity)
  return np.asarray(pil_img) #/ 255.

class GTA5DataSet(data.Dataset):
    def __init__(self, root, list_path, max_iters=None, crop_size=(321, 321), mean=(128, 128, 128), scale=True, mirror=True, ignore_label=255, aug=False):
        self.root = root
        self.list_path = list_path
        self.crop_size = crop_size
        self.scale = scale
        self.ignore_label = ignore_label
        self.mean = mean
        self.aug = aug

        self.aug_policy = augmentations.augmentations
        self.aug_noise = augmentations.noise_augment
        self.aug_style = augmentations.style_augment
        #'1280,720'
        #self.shorter = [243, 360, 486, 612, 720, 828, 954, 1080, 1197]
        self.shorter = [240, 360, 480, 600, 720, 840]
        self.aspect = [1280 / 720-0.2, 1280 / 720-0.1,1280 / 720, 1280 / 720 + 0.1, 1280 / 720 + 0.2] # range of larger side / shorter side

        self.is_mirror = mirror
        # self.mean_bgr = np.array([104.00698793, 116.66876762, 122.67891434])
        self.img_ids = [i_id.strip() for i_id in open(list_path)]
        self.img_ids = self.img_ids[:24000]
        if not max_iters==None:
            self.img_ids = self.img_ids * int(np.ceil(float(max_iters) / len(self.img_ids)))
        self.files = []

        self.id_to_trainid = {7: 0, 8: 1, 11: 2, 12: 3, 13: 4, 17: 5,
                              19: 6, 20: 7, 21: 8, 22: 9, 23: 10, 24: 11, 25: 12,
                              26: 13, 27: 14, 28: 15, 31: 16, 32: 17, 33: 18}

        # for split in ["train", "trainval", "val"]:
        for name in self.img_ids:
            img_file = osp.join(self.root, "images/%s" % name)
            label_file = osp.join(self.root, "labels/%s" % name)
            self.files.append({
                "img": img_file,
                "label": label_file,
                "name": name
            })

    def __len__(self):
        return len(self.files)


    def __getitem__(self, index):
        datafiles = self.files[index]

        image = Image.open(datafiles["img"]).convert('RGB')
        label = Image.open(datafiles["label"])
        name = datafiles["name"]

        # resize
        if self.scale:
            crop_size = np.random.choice(self.shorter)
            aspect = np.random.choice(self.aspect)
            crop_size = (int(crop_size*aspect), crop_size)
        else:
            crop_size = self.crop_size
        image = image.resize(crop_size, Image.BICUBIC)
        label = label.resize(self.crop_size, Image.NEAREST)
        image = np.asarray(image, np.float32)

        ## Augmentation by mixing images.
        if self.aug:
            d = np.random.randint(1, 4)
            image_aug = image.copy()
            for _ in range(d):
                op = np.random.choice(self.aug_style)
                severity = np.random.randint(1, 10)
                image_aug = apply_op(image_aug, op, severity)
                op = np.random.choice(self.aug_noise)
                severity = np.random.randint(1, 10)
                image_aug = apply_op(image_aug, op, severity)
            m = np.float32(np.random.beta(1., 1.))
            image = (1 - m) * image + m * image_aug
        label = np.asarray(label, np.float32)
        # re-assign labels to match the format of Cityscapes
        label_copy = 255 * np.ones(label.shape, dtype=np.float32)
        for k, v in self.id_to_trainid.items():
            label_copy[label == k] = v

        size = image.shape
        image = image[:, :, ::-1]  # change to BGR
        image -= self.mean
        image = image.transpose((2, 0, 1))

        return image.copy(), label_copy.copy(), np.array(size), name



